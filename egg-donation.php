                <p>The following steps are involved in the female egg donation,</p>
                <P>Step 1: Screening of egg donors is crucial step in egg donation process. It is done in stepwise manner starting with detailed history, followed by examination, ultrasonography of abdomen and then various blood tests, genetic tests and ends psychological analysis of egg donors. Doctors make sure that all egg donors are healthy in all aspects before they donate eggs. Once the donor is finalized, her cycle is matched with patientâ€™s cycle.</P>
                <P>Step 2: Synchronization with the Resident Cycle. In this phase, the egg donor is given contraceptive drugs on the third day of her menstrual cycle(menstrual cycle) to keep her hormones and ovaries functioning.</P>
                <P>Step-3: Supersession of the Cycle is done to prevent ovulation on its own in donors. If they ovulate on their own, doctors wonâ€™t get eggs for IVF.</P>
                <P>Step-4: Stimulation of donors ovaries to produce more follicles(eggs) is done various injections. Even these injections improve quality of follicles and increase its quantity.</P>
                <P>Step-5: Retrieval of Eggs after giving sedation or general anaesthesia is done in IVF Operation Theater after taking maximum sterile precautions. Itâ€™s done with the help of wide bore needle and specialized suction pump. Needle is inserted per vaginally and thus retrieved eggs are immediately transferred in culture media and for analysis in IVF laboratory. All good, matured eggs are fertilized by IVF procedure or by ICSI procedure.</P>
                <P>Step-6:Follow-up of donor is important to prevent any untoward effect. Donors are kept in hospital for few hours for observation and then discharged after few specific instruction. </P>
