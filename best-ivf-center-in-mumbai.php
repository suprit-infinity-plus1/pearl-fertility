                    <p class="h5 mb-30">The first stage in IVF is to put hormones into your body so that you can generate many eggs each month rather than just one. After that, you’ll be evaluated to see if you’re ready for egg retrieval.</p>
                    <br>
                    <p class="h5 mb-30">You will be given injections of a medicine that ripens the developing eggs and initiates the ovulation process prior to the retrieval surgery. The eggs must be extracted shortly as they emerge from the follicles in the ovaries, thus timing is crucial. The eggs will not develop correctly if they are removed too early or too late. Before removing the eggs, your doctor may perform blood tests or an ultrasound to ensure that they are at the proper stage of development. Special instructions will be given to you by the IVF facility the night before and the day of the treatment. The majority of women are offered pain medication and the option of being mildly sedated or being fully anesthetized. </p>
                    <br>
                    <p class="h5 mb-30"> Your doctor will use ultrasound to find follicles in the ovary and a hollow needle to extract the eggs during the procedure. The treatment takes less than 30 minutes on average, but it could take up to an hour. </p>
