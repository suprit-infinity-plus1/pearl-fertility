                    <p class="h5 mb-30"> Your eggs will be blended in the laboratory with your partner’s sperm, which they will have provided on the same day as the retrieval.</p>
                    <br>
                    <p class="h5 mb-30">The fertilized eggs are stored in the clinic under surveillance to ensure optimal growth while you and your spouse go home. You may have to wait up to five days for the embryo to reach the advanced blastocyst stage, depending on the clinic.</p>
